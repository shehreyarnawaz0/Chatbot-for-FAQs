stop_words = set(stopwords.words('english'))

def preprocess(text):
    text = text.lower()
    text = text.translate(str.maketrans('', '', string.punctuation))
    tokens = text.split()  # simple tokenizer instead of word_tokenize
    filtered = [word for word in tokens if word not in stop_words]
    return ' '.join(filtered)

questions = list(faq_data.keys())
processed_questions = [preprocess(q) for q in questions]

vectorizer = TfidfVectorizer()
tfidf_matrix = vectorizer.fit_transform(processed_questions)

def chatbot_response(user_input):
    user_processed = preprocess(user_input)
    user_vector = vectorizer.transform([user_processed])
    similarity = cosine_similarity(user_vector, tfidf_matrix)
    best_index = similarity.argmax()
    best_score = similarity[0][best_index]

    if best_score > 0.3:
        matched_q = questions[best_index]
        return f"**Q:** {matched_q}\n\n**A:** {faq_data[matched_q]}"
    else:
        return "âŒ Sorry, I couldn't find a good match. Try rephrasing your question."

gr.Interface(
    fn=chatbot_response,
    inputs="text",
    outputs="markdown",
    title="ğŸ’¬ Web Design FAQ Chatbot",
    description="Ask me anything about web design â€” UX, tools, coding, SEO, and more!"
).launch()